    private void AcheterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcheterActionPerformed
        // TODO add your handling code here:
        achat = jc.acheter(cc);
        if (achat == true){ // Si la carte est achetée
            SoldeJoueur.setText(jc.getPseudo() + " dispose maintenant de " + jc.getSolde() + "€");
            initCarte(); // Nouvelle carte générée
            afficheCarte(); // Actualisation de la carte
        } else {
            SoldeJoueur.setText(jc.getPseudo() + " a un solde insuffisant ou déjà deux cartes" );
        }
    }//GEN-LAST:event_AcheterActionPerformed

