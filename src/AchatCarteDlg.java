    // Méthodes
    public void remplirListeJoueurs() {
        for (int i = 0; i < ljc.getNbJoueur(); i++) {
            ListeJoueurs.addItem(ljc.getJoueur(i).getPseudo());
        }
    }

    public void initCarte() {
        Panneau.removeAll();
        cc = new CarteLoto(nbCol, nbNum); // Initialisation de la CarteLoto avec le nombre de colonnes et de numéros voulu
        // carte loto instancie ici pour regenrer une carte apres achat ou demande du joueur
        Panneau.setLayout(new java.awt.GridLayout(nbLig, nbCol));
        for (int i = 0; i < this.nbLig; i++) {
            for (int j = 0; j < this.nbCol; j++) {
                JButton bt=new JButton();
                Panneau.add(bt);
            }
        }
    }
    
    public void afficheCarte() {
        for(int i=0; i<this.nbLig; i++) {
            for(int j=0; j<this.nbCol; j++) {
                JButton bt = (JButton)(Panneau.getComponent(i*nbCol+j));
                int val = cc.getCase(i,j) ;
                if(val==0) {
                    bt.setText("");
                } else {
                    bt.setText(""+val);
                }
            }
        }
    }

