    public AchatCarteDlg(java.awt.Frame parent, boolean modal, LesJoueurs list, int nl, int nc, int nn) {
        super(parent, modal);
        initComponents();
        ljc = list; // récupération des valeurs options et liste des joueurs
        nbCol = nc;
        nbLig = nl;
        nbNum = nn;
        achat = false; // achat pas encore effectué
        jc = new Joueur(ljc.getJoueur(0)); // Initialisation de l’attribut jc, au 1er joueur de la liste
        remplirListeJoueurs(); // Remplissage de liste déroulante avec les noms des pseudos des joueurs
        SoldeJoueur.setText(jc.getPseudo() + " dispose de " + jc.getSolde() + "€");
        initCarte(); // création des boutons pour la carte
        afficheCarte(); // Affiche valeurs de la carte sur les boutons
    }

