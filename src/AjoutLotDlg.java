    private void ParcourirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ParcourirActionPerformed
        // TODO add your handling code here:
        JFileChooser jf = new JFileChooser(); // boîte de dialogue prédéfinie, création d’un instance
        // on l’ouvre ici, par la méthode showDialog(…)
        if (jf.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) { // si on a validé, on récupère le chemin complet du fichier sélectionné (path)
             // getSelectedFile() retourne un objet de type File, getPath() donne le chemin pour cet objet
            String path = jf.getSelectedFile().getPath(); // chemin complet;
            // on crée une instance d’une Image avec ce fichier
            Image img = Toolkit.getDefaultToolkit().getImage(path);
            // on affecte au panneau qui va se dessiner
            pan.setImage(img);
        }
    }//GEN-LAST:event_ParcourirActionPerformed

