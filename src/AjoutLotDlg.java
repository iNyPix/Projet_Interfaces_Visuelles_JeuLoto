    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
        // TODO add your handling code here:
        l = new Lot(); // création de l'instance de type joueur
        l.setDescription(descriptif.getText()); // recup description du lot
        if (boncadeau.isSelected()) { // recup la categorie du lot
            l.setCategorie(1);
        } else {
            l.setCategorie(2);
            l.setPhoto(new ImageIcon(pan.getImage()));
        }
        if (nv1.isSelected()) { // recup le niveau du lot
            l.setNiveau(1);
        } else if (nv2.isSelected()) {
            l.setNiveau(2);
        } else {
            l.setNiveau(3);
        }
        ok = true; // indicateur ajouter le lot
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_ValiderActionPerformed

